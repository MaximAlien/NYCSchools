import UIKit

extension UIColor {
    
    class var darkBlue: UIColor {
        return self.init(red: 20.0 / 255.0,
                         green: 42.0 / 255.0,
                         blue: 106.0 / 255.0,
                         alpha: 1.0)
    }
    
    class var lightBlue: UIColor {
        return self.init(red: 17.0 / 255.0,
                         green: 138.0 / 255.0,
                         blue: 212.0 / 255.0,
                         alpha: 1.0)
    }
}
